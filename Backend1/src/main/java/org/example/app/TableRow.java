package org.example.app;

public class TableRow {
    public int jahr;
    public float startKapital;
    public float rendite;
    public float endKapital;

    public TableRow(int jahr, float startKapital, float rendite, float endKapital) {
        this.jahr = jahr;
        this.startKapital = startKapital;
        this.rendite = rendite;
        this.endKapital = endKapital;
    }
}


